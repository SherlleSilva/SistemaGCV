package Negocio;

import java.util.Date;


/**
 *
 * @author Rackell
 */
public class Despesas {
    private long id;
    private double valor;
    private String nome;
    private Date data;
    
    
    public Despesas(long id, double valor, String nome, Date data) {
        this.id = id;
        this.valor = valor;
        this.nome = nome;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public double getValor() {
        return valor;
    }

    public void setValor(double valor) {
        this.valor = valor;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public Date getData() {
        return data;
    }

    public void setData(Date data) {
        this.data = data;
    }
    
    
    
    
    
    
   
}
